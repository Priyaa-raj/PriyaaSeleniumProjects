package week1.day1;

public class CalcOperation {
	
	// Classroom Activity 1
	//These Methods are used in Calculator and Calculator Switch Class
	
	public int addition(int a,int b)
	{
		return a+b;
	}
	public int subraction(int a,int b)
	{
		return a-b;
	}	
	public int multipliation(int a,int b)
	{
		return a*b;
	}
	public int division(int a,int b)
	{
		return a/b;
	}

}
